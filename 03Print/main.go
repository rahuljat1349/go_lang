package main

import "fmt"

func main() {

	age := 25
	name := "Rahul"

	// println gives a space between and also insert a new line
	fmt.Println("name :", name, "age :", age)

	print("Hello World! \n")

	fmt.Printf("Age is : %d\n", age)

	// to know the type of data
	fmt.Printf("type of name is : %T\n", name)


	
}
